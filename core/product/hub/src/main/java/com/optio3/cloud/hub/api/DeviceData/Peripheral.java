package com.optio3.cloud.hub.api.DeviceData;
public class Peripheral {
    public int pI;
    public int dT;
    public long mC;
    public long did;
    public long lS;
    public int hV;

    // Getter and Setter for pI
    public int getPI() {
        return pI;
    }

    public void setPI(int pI) {
        this.pI = pI;
    }

    // Getter and Setter for dT
    public int getDT() {
        return dT;
    }

    public void setDT(int dT) {
        this.dT = dT;
    }

    // Getter and Setter for mC
    public long getMC() {
        return mC;
    }

    public void setMC(long mC) {
        this.mC = mC;
    }

    // Getter and Setter for did
    public long getDid() {
        return did;
    }

    public void setDid(long did) {
        this.did = did;
    }

    // Getter and Setter for lS
    public long getLS() {
        return lS;
    }

    public void setLS(long lS) {
        this.lS = lS;
    }

    // Getter and Setter for hV
    public int getHV() {
        return hV;
    }

    public void setHV(int hV) {
        this.hV = hV;
    }
}
