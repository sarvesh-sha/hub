import java.lang.annotation.Documented;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import com.optio3.logging.Severity;

@Documented
@Retention(RUNTIME)
@Target({ TYPE, METHOD })
public @interface Optio3RequestLogLevel
{
    Severity value();
}
